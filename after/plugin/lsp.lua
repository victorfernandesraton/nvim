    timeout_ms = 10000,
  },
  servers = {
    ['tsserver'] = {'javascript', 'typescript'},
    ['rust_analyzer'] = {'rust'},
    ['ruff_lsp'] = {'python'},
    ['gofmt'] = {'go', 'golang'}
  }
})
